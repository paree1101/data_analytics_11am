# 1) a = 10, b = 3 → a + b
a = 10
b = 3
print(a + b)  # Output: 13

# 2) a = 20, b = 7 → a - b
a = 20
b = 7
print(a - b)  # Output: 13

# 3) Multiply x = 5 and y = 4
x = 5
y = 4
result = x * y
print(result)  # Output: 20

# 4) a = 15, b = 4 → a / b
a = 15
b = 4
print(a / b)  # Output: 3.75

# 5) % operator example (remainder)
a = 17
b = 5
print(a % b)  # Output: 2

# 6) x after +=
x = 10
x += 5
print(x)  # Output: 15

# 7) Comparison: 10 > 5
print(10 > 5)  # Output: True

# 8) Equality check: a == b
a = 8
b = 8
print(a == b)  # Output: True

# 9) Logical expression
print(True and False)  # Output: False

# 10) Exponentiation operator (**)
print(2 ** 3)  # Output: 8
# ** is used for exponentiation (power)

